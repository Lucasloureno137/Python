num = int(input('Insira um número: '))


for i in range(1, 11):
    print(num * i)

    #range(1, 11) gera uma sequência de números de 1 a 10 (o 11 é excluído).
    #O loop for itera sobre cada número dessa sequência (i) 
    # em cada iteração, multiplica o num pelo valor de i, imprimindo o resultado.